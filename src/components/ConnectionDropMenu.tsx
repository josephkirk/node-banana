export function ConnectionDropMenu({
  position,
  handleType,
  connectionType,
  onSelect,
  onClose,
}: ConnectionDropMenuProps) {
  const [selectedIndex, setSelectedIndex] = useState(0);
  const menuRef = useRef<HTMLDivElement>(null);

  // Determine options based on handle type and connection type
  const options = (() => {
    if (handleType === "image") {
      return connectionType === "source" ? IMAGE_TARGET_OPTIONS : IMAGE_SOURCE_OPTIONS;
    }
    if (handleType === "text") {
      return connectionType === "source" ? TEXT_TARGET_OPTIONS : TEXT_SOURCE_OPTIONS;
    }
    if (handleType === "video") {
      return connectionType === "source" ? VIDEO_TARGET_OPTIONS : VIDEO_SOURCE_OPTIONS;
    }
    if (handleType === "audio") {
      return connectionType === "source" ? AUDIO_TARGET_OPTIONS : AUDIO_SOURCE_OPTIONS;
    }
    if (handleType === "easeCurve") {
      return CURVE_OPTIONS;
    }
    if (handleType === "3d") {
      return THREE_D_OPTIONS;
    }
    return [];
  })();

  const handleSelect = useCallback(
    (option: MenuOption) => {
      onSelect({ type: option.type as NodeType, isAction: !!option.isAction });
    },
    [onSelect]
  );

  // Close on click outside
  useEffect(() => {
    const handleClickOutside = (event: MouseEvent) => {
      if (menuRef.current && !menuRef.current.contains(event.target as Node)) {
        onClose();
      }
    };
    document.addEventListener("mousedown", handleClickOutside);
    return () => document.removeEventListener("mousedown", handleClickOutside);
  }, [onClose]);

  // Keyboard navigation
  useEffect(() => {
    const handleKeyDown = (e: KeyboardEvent) => {
      switch (e.key) {
        case "ArrowDown":
          e.preventDefault();
          setSelectedIndex((prev) => (prev + 1) % options.length);
          break;
        case "ArrowUp":
          e.preventDefault();
          setSelectedIndex((prev) => (prev - 1 + options.length) % options.length);
          break;
        case "Enter":
          e.preventDefault();
          if (options[selectedIndex]) {
            handleSelect(options[selectedIndex]);
          }
          break;
        case "Escape":
          e.preventDefault();
          onClose();
          break;
      }
    };
    window.addEventListener("keydown", handleKeyDown);
    return () => window.removeEventListener("keydown", handleKeyDown);
  }, [options, selectedIndex, handleSelect, onClose]);

  if (options.length === 0) return null;

  // Adjust position to keep menu on screen
  const menuWidth = 200;
  const menuHeight = options.length * 36 + 40;
  let x = position.x;
  let y = position.y;

  if (x + menuWidth > window.innerWidth) x -= menuWidth;
  if (y + menuHeight > window.innerHeight) y -= menuHeight;

  return (
    <div
      ref={menuRef}
      className="fixed z-[1000] bg-neutral-800 border border-neutral-600 rounded-lg shadow-2xl py-1 min-w-[180px] overflow-hidden animate-in fade-in zoom-in duration-100"
      style={{ left: x, top: y }}
    >
      <div className="px-3 py-1.5 border-b border-neutral-700 bg-neutral-900/50">
        <p className="text-[10px] font-bold text-neutral-500 uppercase tracking-wider">
          Suggested Nodes ({handleType})
        </p>
      </div>
      <div className="max-h-[300px] overflow-y-auto custom-scrollbar">
        {options.map((option, index) => (
          <button
            key={`${option.type}-${index}`}
            onClick={() => handleSelect(option)}
            onMouseEnter={() => setSelectedIndex(index)}
            className={`w-full flex items-center gap-3 px-3 py-2 text-sm transition-colors ${
              index === selectedIndex
                ? "bg-blue-600 text-white"
                : "text-neutral-300 hover:bg-neutral-700"
            }`}
          >
            <div className={`${index === selectedIndex ? "text-white" : "text-neutral-400"}`}>
              {option.icon}
            </div>
            <span className="flex-1 text-left">{option.label}</span>
          </button>
        ))}
      </div>
      <div className="px-3 py-1.5 border-t border-neutral-700 bg-neutral-900/30 flex items-center justify-between">
        <span className="text-[9px] text-neutral-500">
          <kbd className="px-1 py-0.5 bg-neutral-700 rounded text-[8px]">↑↓</kbd> navigate
        </span>
        <span className="text-[9px] text-neutral-500">
          <kbd className="px-1 py-0.5 bg-neutral-700 rounded text-[8px]">↵</kbd> select
        </span>
      </div>
    </div>
  );
}
